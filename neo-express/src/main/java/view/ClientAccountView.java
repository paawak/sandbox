    private void setFieldsToModify(ClientAccountObject clObj){
        if(clObj==null){
            JOptionPane.showMessageDialog(this,"Please enter proper search criteria.","No match found:",JOptionPane.INFORMATION_MESSAGE);
            System.out.println("Could not create an instance of ClientAccountObject");
            return ;
        }
        
        txtAccNum.setText(clObj.getAccNum().toString());
        txtName.setText(clObj.getName().toString());
        txtShortName.setText(clObj.getShortName().toString());
        txtContactFirstName.setText(clObj.getContactFirstName().toString());
        txtContactMiddleName.setText(clObj.getContactMiddleName().toString());
        txtContactLastName.setText(clObj.getContactLastName().toString());
        txtTelephone1.setText(clObj.getTelephone1().toString());
        txtTelephone2.setText(clObj.getTelephone2().toString());
        txtMobile.setText(clObj.getMobile().toString());
        txtFax.setText(clObj.getFax().toString());
        txtEmail.setText(clObj.getEmail().toString());
        txtAddress1.setText(clObj.getAddress1().toString());
        txtAddress2.setText(clObj.getAddress2().toString());
        txtCity.setText(clObj.getCity().toString());
        txtState.setText(clObj.getState().toString()); 
        if(clObj.getIsOrganization().toString().trim().equals("1"))
            rdBtOrganisation.setSelected(true);
        else
            rdBtIndividual.setSelected(true);
    }
    
    /**
     *initialises modification of records
     */
    private void saveChangesButtonPressed(){
        if(isDataValid()){
            ClientAccountObject clObj = getValuesFromInput();
            if(clObj==null){
                JOptionPane.showMessageDialog(this,"Data could not be modified","Sorry",JOptionPane.ERROR_MESSAGE);
                return ;
            }
            update(clObj);
            resetFields();
        }        
    }
    
    /** processes mouse released events on 'view record'
     * button on the search panel
     *
     */
    private void btSearchPanelViewRecordMouseReleased(java.awt.event.MouseEvent evt) {
        boolean id;
        if(srchPnl.cbSearch.getSelectedIndex()==0)
            id=false;
        else
            id=true;
        
        setFieldsToModify(clientAccountController.getClientObj(srchPnl.txtSearch.getText(),id));        
    }
    
