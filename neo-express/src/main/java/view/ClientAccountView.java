    private void saveButtonPressed(){
        if(isDataValid()){
            ClientAccountObject clObj = getValuesFromInput();
            if(clObj==null){
                JOptionPane.showMessageDialog(this,"Data could not be saved","Sorry",JOptionPane.ERROR_MESSAGE);
                return ;
            }
            save(clObj);
        }
    }
    
