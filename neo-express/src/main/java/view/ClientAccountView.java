    /**
     *check data validation
     */
    private  boolean isDataValid(){
        if(txtName.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"Please enter the Full Name.","'Full Name' is vacant:",JOptionPane.INFORMATION_MESSAGE);
            txtName.requestFocus();
            return false;
        }
        else if(txtContactFirstName.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this,"Please enter the First Name.","'First Name' is vacant:",JOptionPane.INFORMATION_MESSAGE);
            txtContactFirstName.requestFocus();
            return false;
        }
          else if(txtContactLastName.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(this,"Please enter the Last Name.","'Last Name' is vacant:",JOptionPane.INFORMATION_MESSAGE);
                    txtContactLastName.requestFocus();
                    return false;
                }
          else if(txtTelephone1.getText().trim().equals("") && txtTelephone2.getText().trim().equals("") && txtMobile.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(this,"Please enter the Telephone No.","'Telephone 1' is vacant:",JOptionPane.INFORMATION_MESSAGE);
                    txtTelephone1.requestFocus();
                    return false;
                }
          else if(txtAddress1.getText().trim().equals("") && txtAddress2.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(this,"Please enter the Address.","'Address' is vacant:",JOptionPane.INFORMATION_MESSAGE);
                    txtAddress1.requestFocus();
                    return false;
                }
          else if(txtCity.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(this,"Please enter the City.","'City' is vacant:",JOptionPane.INFORMATION_MESSAGE);
                    txtCity.requestFocus();
                    return false;
                }
          else if(txtState.getText().trim().equals("")){
                    JOptionPane.showMessageDialog(this,"Please enter the State.","'State' is vacant:",JOptionPane.INFORMATION_MESSAGE);
                    txtState.requestFocus();
                    return false;
                }
          else{
              return true;
          }
            }
    
    /** to get data from the user's end
     *
     */
    private ClientAccountObject getValuesFromInput() {
        Object[] dataArr = new Object[17];
        dataArr[0] = txtAccNum.getText();
        dataArr[1] = txtName.getText();
        dataArr[2] = txtShortName.getText();
        dataArr[3] = txtContactFirstName.getText();
        dataArr[4] = txtContactMiddleName.getText();
        dataArr[5] = txtContactLastName.getText();
        dataArr[6] = txtTelephone1.getText();
        dataArr[7] = txtTelephone2.getText();
        dataArr[8] = txtMobile.getText();
        dataArr[9] = txtFax.getText();
        dataArr[10] = txtEmail.getText();
        dataArr[11] = txtAddress1.getText();
        dataArr[12] = txtAddress2.getText();
        dataArr[13] = txtCity.getText();
        dataArr[14] = txtState.getText();
        if(rdBtOrganisation.isSelected())
            dataArr[15] = "1";
        else
            dataArr[15] = "0";
        dataArr[16] = "1";//status
        //pass on the data, in form of the model to the controller
        ClientAccountObject clObj = null;
        try{
            clObj = new ClientAccountObject(dataArr);        
        }catch(Exception e ){
            System.out.println(e);
        }      
        
        return clObj;
    }  
    
    private void save(ClientAccountObject clObj){
        //pass on the data, in form of the model to the controller
            clientAccountController.saveInDB(this, clObj);
    }
    
    private void update(ClientAccountObject clObj){
        clientAccountController.updateRecord(this, clObj);
    }
    
